# String definition
# Def: A string is a sequence of characters. The class String includes methods for examining individual characters,
# comparing strings, searching strings, extracting parts of strings, and for converting an entire string uppercase
# and lowercase. String objects are immutable, meaning that they cannot be changed once they have been created.
# For example, the word "hamburger" and the phrase "I ate 3 hamburgers" are both strings. Even "12345" could be
# considered a string, if specified correctly (Enclosing the numbers with double quotes: "12345").

# operations that can be performed on strings
# 1. Concatenation (+): Combines two strings together.
# 2. Repetition (*): Repeats the string multiple times.
# 3. Slicing ([]): Extracts a portion of the string based on index.
# 4. Range Slicing ([:]): Extracts a substring within a specified range.
# 5. Membership (in): Checks if a substring is present in the string.
# 6. Iteration (for): Iterates over each character in the string.
# 7. Length (len()): Returns the number of characters in the string.
# 8. String Formatting (%): Formats a string using placeholders.
# 9. Capitalize (capitalize()): Converts the first character to uppercase.
# 10. Center (center()): Centers the string within a specified width.
# 11. Count (count()): Counts occurrences of a substring in the string.
# 12. Encode (encode()): Encodes the string into bytes using a specified encoding.
# 13. Decode (decode()): Decodes bytes into a string using a specified encoding.
# 14. Endswith (endswith()): Checks if the string ends with a specified suffix.
# 15. Startswith (startswith()): Checks if the string starts with a specified prefix.
# 16. Expandtabs (expandtabs()): Expands tabs in the string to spaces.
# 17. Find (find()): Returns the index of the first occurrence of a substring.
# 18. Index (index()): Returns the index of the first occurrence, raises an exception if not found.
# 19. Isalnum (isalnum()): Checks if all characters are alphanumeric.
# 20. Isalpha (isalpha()): Checks if all characters are alphabetic.
# 21. Isdigit (isdigit()): Checks if all characters are digits.
# 22. Islower (islower()): Checks if all characters are lowercase.
# 23. Isupper (isupper()): Checks if all characters are uppercase.
# 24. Isnumeric (isnumeric()): Checks if all characters are numeric.
# 25. Isspace (isspace()): Checks if all characters are whitespace.
# 26. Isprintable (isprintable()): Checks if all characters are printable.
# 27. Join (join()): Joins elements of an iterable using the string as a separator.
# 28. Lower (lower()): Converts all characters to lowercase.
# 29. Upper (upper()): Converts all characters to uppercase.
# 30. Swapcase (swapcase()): Swaps the case of each character.
# 31. Title (title()): Converts the first character of each word to uppercase.
# 32. Lstrip (lstrip()): Removes leading whitespace characters.
# 33. Rstrip (rstrip()): Removes trailing whitespace characters.
# 34. Strip (strip()): Removes leading and trailing whitespace characters.
# 35. Max (max()): Returns the character with the highest ASCII value.
# 36. Min (min()): Returns the character with the lowest ASCII value.
# 37. Replace (replace()): Replaces occurrences of a substring with another substring.
# 38. Split (split()): Splits the string into a list of substrings based on a delimiter.
# 39. Splitlines (splitlines()): Splits the string at line breaks and returns a list of lines.
# 40. Zfill (zfill()): Pads the string with zeros on the left to a specified width.
# 41. Maketrans (maketrans()): Creates a translation table for use in translate() method.
# 42. Translate (translate()): Translates the string using a translation table.
# 43. Partition (partition()): Splits the string at the first occurrence of a separator.
# 44. Rpartition (rpartition()): Splits the string at the last occurrence of a separator.
# 45. Rfind (rfind()): Returns the highest index of the first occurrence of a substring.
# 46. Rindex (rindex()): Returns the highest index of the first occurrence, raises an exception if not found.
# 47. Rjust (rjust()): Right-justifies the string within a specified width.
# 48. Rsplit (rsplit()): Splits the string into a list of substrings from the right based on a delimiter.

