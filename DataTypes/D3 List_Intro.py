# List Definition
# A list is a collection which is ordered and changeable. In Python lists are written with square brackets.

# Create a List:
thislist = ["apple", "banana", "cherry"]
print(thislist)

# List operations
# 1. Append (append()): Adds an element to the end of the list.
# 2. Extend (extend()): Extends the list by appending elements from another iterable.
# 3. Insert (insert()): Inserts an element at a specified index in the list.
# 4. Remove (remove()): Removes the first occurrence of a specified element from the list.
# 5. Pop (pop()): Removes and returns the element at a specified index (default is the last element).
# 6. Clear (clear()): Removes all elements from the list.
# 7. Index (index()): Returns the index of the first occurrence of a specified element.
# 8. Count (count()): Returns the number of occurrences of a specified element in the list.
# 9. Sort (sort()): Sorts the elements of the list in ascending order.
# 10. Reverse (reverse()): Reverses the order of elements in the list.
# 11. Copy (copy()): Returns a shallow copy of the list.
# 12. List Comprehension: A concise way to create lists using a single line of code.
# 13. Membership Test (in): Checks if an element is present in the list.
# 14. Length (len()): Returns the number of elements in the list.
# 15. Maximum (max()): Returns the maximum element in the list.
# 16. Minimum (min()): Returns the minimum element in the list.
# 17. Sum (sum()): Returns the sum of all elements in the list.
# 18. Slicing ([]): Extracts a portion of the list based on index.
# 19. Range Slicing ([:]): Extracts a sublist within a specified range.
# 20. Concatenation (+): Concatenates two lists together.
# 21. Repetition (*): Repeats the list multiple times.
# 22. List Membership Test (in): Checks if a sublist is present in the list.
# 23. Iteration (for): Iterates over each element in the list.
# 24. Any (any()): Returns True if at least one element in the list is True.
# 25. All (all()): Returns True if all elements in the list are True.
# 26. Filter (filter()): Filters elements of the list based on a function.
# 27. Map (map()): Applies a function to each element of the list.
# 28. Reduce (functools.reduce()): Applies a function cumulatively to the elements of the list.
# 29. Zip (zip()): Combines elements from multiple iterables into tuples.
# 30. Enumerate (enumerate()): Returns tuples containing indices and elements from the list.
# 31. List to Set Conversion (set()): Converts the list into a set.
# 32. List to Tuple Conversion (tuple()): Converts the list into a tuple.
# 33. List to String Conversion (join()): Joins list elements into a string.
